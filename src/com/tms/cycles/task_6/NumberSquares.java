package com.tms.cycles.task_6;

/**
 * Task:
 *
 * Write a program that displays the squares of numbers from 10 to 20 inclusive.
 */

public class NumberSquares {

    public static void main(String[] args) {

        for (int i = 10; i <= 20; ++i) {
            System.out.print(i * i + " ");
        }

    }

}
