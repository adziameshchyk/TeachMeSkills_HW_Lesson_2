package com.tms.cycles.task_2;

/**
 *Task:
 *
 * It is necessary to display numbers from 5 to 1 on the screen.
 * When solving, use the decrement (--) operator.
 */

public class FiveToOne {

    public static void main(String[] args) {

        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }

    }

}
